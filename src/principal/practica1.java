package principal;

public class practica1 {
    public static void main(String args[]) {
        int a = 3;
        int b = 4;

        if (a > b) {
            System.out.println(a + " es mayor que " + b);
        } else {
            System.out.println(a + " no es mayor que " + b);
        }
        if (a < b) {
            System.out.println(a + " es menor que " + b);
        } else {
            System.out.println(a + " no es menor que " + b);
        }
        if (a >= b) {
            System.out.println(a + " es mayor o igual que " + b);
        } else {
            System.out.println(a + " no es mayor o igual que " + b);
        }
        if (a <= b) {
            System.out.println(a + " es menor o igual que " + b);
        } else {
            System.out.println(a + " no es menor o igual que " + b);
        }
        if (a != b) {
            System.out.println(a + " es diferente de " + b);
        } else {
            System.out.println(a + "no es diferente de " + b);
        }
    }
}

