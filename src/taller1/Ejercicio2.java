/*Realice un programa que permita calcular el área de un triángulo conociendo su base y su
altura.*/

package taller1;


public class Ejercicio2 {
    public static void main(String args[]) {
        float area = 0;
        float base = 4;
        float altura = 3;

        area = (base * altura) / 2;
        System.out.println("El area de un triangulo es: " + area);
    }
}
